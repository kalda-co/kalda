<article class="content">
  <h2>
    If you’re at risk of suicide, or you need someone to talk to right now, use
    the contacts below.
  </h2>
  <div class="text-card">
    <h1>Text message support</h1>
    <p>
      If you’re in a mental health crisis you can speak to someone via text.
      Text ‘Shout’ to 85258. Shout are free and available 24/7.
    </p>
    <a href="https://giveusashout.org/" target="_blank" rel="noopener"
      >Shout Website</a
    >
  </div>
  <div class="text-card">
    <h1>Phone support</h1>
    <p>
      Phone the Samaritans on 116 123. Whatever you're going through, a
      Samaritan will face it with you. They are free and available 24/7.
    </p>
    <a href="https://www.samaritans.org/" target="_blank" rel="noopener"
      >The Samaritans</a
    >
  </div>
  <div class="text-card last">
    <h1>NHS Support</h1>
    <p>
      Phone NHS 111 if you need medical attention, including if you are at risk
      of suicide. You will be able to answer questions about your main sympton
      and get NHS medical help near you. For emergencies phone 999.
    </p>
    <a
      href="https://www.nhs.uk/conditions/stress-anxiety-depression/mental-health-helplines/"
      target="_blank"
      rel="noopener">NHS Website</a
    >
  </div>
</article>

